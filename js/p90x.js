/*
	init the working program object which is basically a clone of the template
	object and some meta data + functions from the prototype
*/
function Program (template, storeName) {

	var savedProgram = JSON.parse(localStorage.getItem(storeName));

	if(savedProgram) {
		this.program = savedProgram;
	} else {
		this.program = template.program;
		this.program.metadata = { "activeWorkout": 0, "storeName": storeName };
	}
	this.logger = this.logger();
}

Program.prototype = {
	toString: function() {
		var storeName = this.program.metadata.storeName,
			savedProgram = this.readState();
		console.log(storeName, savedProgram || "Nothing saved yet");
	},
	editLog: function (workoutIdx, exerciseIdx, data, index) {
		console.log(arguments);
		var curExercise = this.program.workouts[workoutIdx].exercises[exerciseIdx];

		if(!curExercise.datalog) {
			curExercise.datalog = [];
		}

		if(index) {
			curExercise.datalog[index] = data;
		} else {
			curExercise.datalog.push(data);
		}

		console.log("datalog", curExercise.datalog);

		return curExercise.datalog;
	},
	log: function (data) {
		var md = this.program.metadata,
			wk = this.program.workouts[md.activeWorkout];
		return this.editLog( md.activeWorkout, wk.activeExercise, data );
	},
	logger: function() {
		var _this = this,
			logger = function(e) {

				var $elem = $(this).closest('form'),
					$inputs = $elem.find('input'),
					data = {},
					datalog;

				data.date = new Date().toString();
				data.data = {};

				$inputs.each(function(k) {
					var $i = $(this);
					data.data[$i.attr('name')] = $i.val();
				});

				console.log('data to be saved:', data);
				datalog = _this.log(data);
				
				$elem.hide().siblings('.alert-success').show();
				_this.refreshHistory($elem.parent(), datalog);
				_this.next();
				_this.saveState();
				
				setTimeout(function() {
					_this.updateProgress($('#nav .bar'));
					_this.swipe.next();
				}, 100);
				
			};
		return logger;
	},
	next: function() {
		var md = this.program.metadata,
			wk = this.program.workouts[md.activeWorkout];

		if ( wk.activeExercise === wk.exercises.length ) {
			// done with this workout!
			md.activeWorkout = ( md.activeWorkout + 1 ) % md.workouts.length;
			wk.activeExercise = 0;

			console.log('all done! congrats!');
		} else {
			wk.activeExercise += 1;

			console.log('activeExercise:', wk.activeExercise);
		}
	},
	saveState: function() {
		localStorage.setItem(this.program.metadata.storeName, JSON.stringify(this.program));
	},
	readState: function() {
		return JSON.parse(localStorage.getItem(this.program.metadata.storeName));
	},
	clear: function() {
		localStorage.removeItem(this.program.metadata.storeName);
	},
	refreshHistory: function(elem, datalog) {
		var $elem = $(elem),
			$history = $(elem).find('.history').html(''),
			$row,
			len = datalog.length,
			text = "";

		if(!$history.length) {
			$history = $('<table class="history table table-striped"><thead><tr><th>#</th><th>Date</th><th>Log</th><tbody></tbody></table>'),
			$elem.append($history);

			$history = $history.find('tbody');
		}

		$.each(datalog, function(k, v) {
			$row = $('<tr></tr>');

			$row.append('<td>' + (len-k) + '</td>');
			$row.append('<td>' + v.date + '</td>');

			text = "";
			$.each(v.data, function(k, v) {
				text += k.charAt(0).toUpperCase() + k.slice(1) + ": " + v + "<br/>";
			});

			$row.append('<td>' + text + '</td>');

			$history.append($row);
		});
		
		$history.html($history.find('tr').get().reverse());
	},
	updateProgress: function(elem) {
		var $elem = $(elem),
			md = this.program.metadata,
			wk = this.program.workouts[md.activeWorkout],
			width = Math.ceil(wk.activeExercise / wk.exercises.length * 100);

		console.log('progress:', width);
		$elem.width(width + '%');
	},
	loadWorkout: function(el, index, exercise) {
		var $temp,
			$list = $('<ul></ul>'),
			wk = this.program.workouts[index],
			_this = this;

		this.program.metadata.activeWorkout = index;
		if(!exercise) {
			wk.activeExercise = 0;
		}
		
		$.each(wk.exercises, function(k, v) {
			$temp = $('<li class="exercise"></li>');
			$temp.append("<h3>" + v.name + "</h3>");

			var $form = $('<form class="form-horizontal"></form>');

			$.each(v.options, function(k, v) {
				var prettyName = v.charAt(0).toUpperCase() + v.slice(1);
				$form.append('<div class="control-group"><label class="control-label" for="' + v + '">' + prettyName + '</label><div class="controls"><input type="text" class="input-medium" name="' + v + '" placeholder="' + prettyName + '"/></div></div>');
			});

			$form.append('<div class="control-group"><div class="controls"><span class="save btn btn-primary">Save</span><span class="skip btn btn-link pull-right">Skip</span></div></div>');
			$temp.append($form);

			$temp.append('<div class="alert alert-success">Good Job!</div>');
			console.log(v);
			if(v.datalog) {
				_this.refreshHistory($temp, v.datalog);
			}
			$list.append($temp);
		});

		$(el).append($list);
		$('#menu').hide();
		$('#nav').show();
		$('#nav-bottom').show();
		
		this.swipe = new Swipe(document.getElementById('slider'), { "startSlide": wk.activeExercise });
		this.updateProgress($('#nav .bar'));

	}
};

